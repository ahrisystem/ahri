package model.cadastrosUnicos;

public class LicencaModel {
    private String tipoLicenca;
    private int nadaConsta;
    private String cnpj;

    public String getTipoLicenca() {
        return tipoLicenca;
    }

    public void setTipoLicenca(String tipoLicenca) {
        this.tipoLicenca = tipoLicenca;
    }

    public int getNadaConsta() {
        return nadaConsta;
    }

    public void setNadaConsta(int nadaConsta) {
        this.nadaConsta = nadaConsta;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }
    
    
}
